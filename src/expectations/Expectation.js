//-------------------------------------------------------------------------------
// Imports
//-------------------------------------------------------------------------------

import _ from 'lodash';
import {
    Obj
} from 'object-js';


//-------------------------------------------------------------------------------
// Class
//-------------------------------------------------------------------------------

/**
 * @class
 * @extends {Obj}
 */
export default class Expectation extends Obj {
    
    //-------------------------------------------------------------------------------
    // Constructor
    //-------------------------------------------------------------------------------

    /**
     * @constructs
     * @param {string} type
     * @param {string=} message
     */
    constructor(type, message) {
        
        super();


        //-------------------------------------------------------------------------------
        // Private Properties
        //-------------------------------------------------------------------------------

        /**
         * @private
         * @type {string}
         */
        this.message            = _.isString(message) ? message : '';

        /**
         * @private
         * @type {string}
         */
        this.type               = type;
    }
    
    
    //-------------------------------------------------------------------------------
    // Getters and Setters
    //-------------------------------------------------------------------------------

    /**
     * @return {string}
     */
    getMessage() {
        return this.message;
    }
    
    /**
     * @return {string}
     */
    getType() {
        return this.type;
    }
}
