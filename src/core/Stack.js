//-------------------------------------------------------------------------------
// Imports
//-------------------------------------------------------------------------------

import {
    Obj
} from 'object-js';
import _ from 'lodash';


//-------------------------------------------------------------------------------
// Class
//-------------------------------------------------------------------------------

/**
 * @class
 * @extends {Obj}
 */
export default class Stack extends Obj {

    //-------------------------------------------------------------------------------
    // Constructor
    //-------------------------------------------------------------------------------

    /**
     * @constructs
     * @param {Array<StackFrame>=} frames
     */
    constructor(frames) {

        super();


        //-------------------------------------------------------------------------------
        // Private Properties
        //-------------------------------------------------------------------------------

        /**
         * @private
         * @type {Array<StackFrame>}
         */
        this.frames = frames || [];
    }


    //-------------------------------------------------------------------------------
    // Getters and Setters
    //-------------------------------------------------------------------------------

    /**
     * @return {Array<StackFrame>}
     */
    getFrames() {
        return this.frames;
    }


    //-------------------------------------------------------------------------------
    // Public Methods
    //-------------------------------------------------------------------------------

    /**
     * @return {string}
     */
    toString() {
        return _.join(this.frames, '\n');
    }
}
