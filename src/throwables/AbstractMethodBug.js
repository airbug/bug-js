//-------------------------------------------------------------------------------
// Imports
//-------------------------------------------------------------------------------

import {
    Bug
} from '../core';


//-------------------------------------------------------------------------------
// Class
//-------------------------------------------------------------------------------

/**
 * @class
 * @extends {Bug}
 */
export default class AbstractMethodBug extends Bug {

    //-------------------------------------------------------------------------------
    // Static Properties
    //-------------------------------------------------------------------------------

    /**
     * @static
     * @const {string}
     */
    static NOT_IMPLEMENTED = 'AbstractMethodBug:NotImplemented';


    //-------------------------------------------------------------------------------
    // Constructor
    //-------------------------------------------------------------------------------

    /**
     * @constructs
     * @param {string} type
     * @param {string} methodName
     * @param {string} message
     * @param {Array.<(Throwable | Error)>=} causes
     * @private
     */
    constructor(type, methodName, message, causes) {

        super(type, {}, message, causes);


        //-------------------------------------------------------------------------------
        // Private Properties
        //-------------------------------------------------------------------------------

        /**
         * @private
         * @type {string}
         */
        this.methodName     = methodName;
    }


    //-------------------------------------------------------------------------------
    // Getters and Setters
    //-------------------------------------------------------------------------------

    /**
     * @return {string}
     */
    getMethodName() {
        return this.methodName;
    }
}
